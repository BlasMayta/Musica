        <li><strong>Paso 3:</strong> Trata de marcar el ritmo con tus palmas o pies.</li>
        <li><strong>Paso 4:</strong> Escucha cómo se mezclan las voces o los instrumentos y piensa en eso como la armonía que hace que la canción sea única.</li>
