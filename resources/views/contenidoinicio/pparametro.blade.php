      <p>
        La música es como un gran cuento que se cuenta con sonidos. Para entenderlo mejor, aprendemos a identificar tres partes muy importantes, llamadas parámetros: <span class="important">Intensidad</span>, <span class="important">Ritmo</span> y <span class="important">Armonía</span>. Conocer estos parámetros te ayudará a escuchar la música de forma divertida y a descubrir todos sus secretos.
      </p>
    </div>
    
    <!-- 1. Intensidad -->
    <div class="section">
      <h2>1. Intensidad</h2>
      <h3>¿Qué es la Intensidad?</h3>
      <p>
        La intensidad es el volumen o la fuerza con la que suena la música. Es lo que nos permite saber si un sonido es muy fuerte o muy suave.
      </p>
      <p><strong>Ejemplo Ampliado:</strong></p>
      <ul>
        <li>Imagina que tienes un tambor en casa.</li>
        <li>Si lo golpeas fuerte, el sonido es muy potente, y se dice que tiene alta intensidad.</li>
        <li>Si lo tocas suavemente, el sonido es más tranquilo, lo que se llama baja intensidad.</li>
      </ul>
      <p>
        <strong>¿Por qué es importante?</strong> La intensidad puede cambiar la emoción de la música. Una canción con sonidos fuertes puede hacerte sentir emocionado o enérgico, mientras que una canción suave puede ayudarte a sentirte calmado o reflexivo.
      </p>
    </div>
    
    <!-- 2. Ritmo -->
    <div class="section">
      <h2>2. Ritmo</h2>
      <h3>¿Qué es el Ritmo?</h3>
      <p>
        El ritmo es como el latido de un corazón en la música. Es la secuencia de sonidos y silencios que se repite y le da orden a la canción.
      </p>
      <p><strong>Ejemplo Ampliado:</strong></p>
      <ul>
        <li>Imagina que estás en una clase de música y el maestro pone música.</li>
        <li>Cuando aplaudes o golpeas tus pies siguiendo la música, estás siguiendo el ritmo.</li>
        <li>Es similar a contar "1, 2, 3, 4" de manera regular mientras caminas; ese "contar" es el ritmo.</li>
      </ul>
      <p>
        <strong>¿Por qué es importante?</strong> El ritmo te invita a moverte, a bailar y a sentir la música en tu cuerpo. Además, te ayuda a recordar la canción y a tocar instrumentos en grupo, ya que todos deben seguir el mismo ritmo.
      </p>
    </div>
    
    <!-- 3. Armonía -->
    <div class="section">
      <h2>3. Armonía</h2>
      <h3>¿Qué es la Armonía?</h3>
      <p>
        La armonía es la forma en que diferentes sonidos o notas se juntan para formar una melodía bonita. Es como cuando varios amigos cuentan un cuento, cada uno con su parte, y juntos hacen una historia completa.
      </p>
      <p><strong>Ejemplo Ampliado:</strong></p>
      <ul>
        <li>Imagina que tú y un amigo están cantando al mismo tiempo.</li>
        <li>Si uno canta una nota baja y el otro una nota alta, y al juntarlas suenan bien, eso es armonía.</li>
        <li>Otra forma de verlo es pensar en una orquesta: la guitarra, el piano y el violín tocan distintos sonidos, pero cuando se unen, crean una música muy agradable.</li>
      </ul>
      <p>
        <strong>¿Por qué es importante?</strong> La armonía enriquece la música. Hace que la canción suene más completa y especial, dándonos una experiencia más bonita al escucharla.
      </p>
    </div>
    
    <!-- Actividad para Recordar -->
    <div class="section">
      <h2>Actividad para Recordar: Escucha y Descubre</h2>
      <ul>
        <li><strong>Paso 1:</strong> Escoge tu canción favorita.</li>
