    <!-- Introducción -->
    <div class="section">
      <h2>¿Qué es la Técnica Vocal?</h2>
      <p>
        La técnica vocal es la forma de usar y cuidar nuestra voz para cantar y hablar sin lastimarnos. Cuando aprendemos técnica vocal, descubrimos cómo ponernos de pie, respirar y pronunciar los sonidos de forma clara y saludable.
      </p>
    </div>
    
    <!-- 1. Postura Correcta -->
    <div class="section">
      <h2>1. Postura Correcta</h2>
      <ul>
        <li><span class="important">Espalda recta:</span> Mantén la cabeza erguida y los hombros relajados.</li>
        <li><span class="important">Pies separados:</span> Coloca los pies a la altura de los hombros para tener equilibrio.</li>
        <li><span class="important">Relajación:</span> Mueve suavemente el cuello y los hombros para soltar tensiones.</li>
      </ul>
      <p><strong>¿Por qué es importante?</strong> Si nos paramos derechos, podemos respirar mejor y no forzamos la voz al cantar o hablar.</p>
    </div>
    
    <!-- 2. Respiración Diafragmática -->
    <div class="section">
      <h2>2. Respiración Diafragmática</h2>
      <ul>
        <li>Coloca una mano en tu abdomen y otra en tu pecho.</li>
        <li><span class="important">Inhala (mete aire):</span> Siente primero cómo se infla tu abdomen antes de que suba tu pecho.</li>
        <li><span class="important">Exhala (saca aire):</span> Hazlo lentamente, como un suave silbido “ssss” o “shhh”.</li>
      </ul>
      <p><strong>¿Por qué es importante?</strong> Esta forma de respirar te ayuda a controlar el aire que usas al cantar o hablar, evitando que te quedes sin respiración.</p>
    </div>
    
    <!-- 3. Emisión de la Voz -->
    <div class="section">
      <h2>3. Emisión de la Voz</h2>
      <ul>
        <li><span class="important">Vocaliza con sílabas:</span> Por ejemplo, “ma-me-mi-mo-mu”, intentando que suenen claras y sin apretar la garganta.</li>
        <li><span class="important">Hacer el sireno:</span> Desliza tu voz desde sonidos graves (bajos) hasta sonidos agudos (altos) de forma suave.</li>
        <li><span class="important">Llamar y Responder:</span> El maestro canta una pequeña frase musical y tú la repites igual, fijándote en la melodía y el ritmo.</li>
      </ul>
      <p><strong>¿Por qué es importante?</strong> Con estos ejercicios mejoras la afinación (entonación) y la claridad de tu voz, además de aprender a controlar el paso de un sonido grave a uno agudo.</p>
    </div>
    
    <!-- 4. Cuidado de la Voz -->
    <div class="section">
      <h2>4. Cuidado de la Voz</h2>
      <ul>
        <li><span class="important">Bebe agua:</span> Mantén tu garganta hidratada.</li>
        <li><span class="important">No grites:</span> Forzar la voz puede lastimarla.</li>
        <li><span class="important">Practica un poco cada día:</span> Así tu voz se vuelve más fuerte y sana.</li>
      </ul>
      <p><strong>¿Por qué es importante?</strong> Cuidar tu voz te permite cantar y hablar mejor, evitando molestias y problemas en la garganta.</p>
